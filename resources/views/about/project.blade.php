<p><b>กรอบแนวคิดในงานวิจัย</b><br>
&emsp;&emsp;&emsp;กรอบแนวคิดในงานวิจัยมีปัจจัยนำเข้าระบบคือ ข้อมูลหน่วยงานมหาวิทยาลัย ข้อมูลพื้นที่ชุมชนและปัญหาชุมชน
ข้อมูลนักวิจัย งานวิจัย บทความวิจัย ผู้เชี่ยวชาญ และผลการใช้ประโยชน์ ซึ่งจะถูกรวบรวมรูปแบบฐานข้อมูลที่มีความสัมพันธ์กัน
และพัฒนาระบบจัดการฐานข้อมูลเพื่อสนับสนุนผู้ใช้และประมวลผลเพื่อให้ได้ข้อมูลที่นำมาใช้สนับสนุนการตัดสินใจและบริหารจัดการ
เพื่อแก้ไขปัญหาของชุมชนได้ตรงกับสภาพความเป็นจริงของชุมชน จะทำให้เกิดความร่วมมือและทำให้ชุมชนท้องถิ่นน่าอยู่ขึ้น<br>
&emsp;&emsp;&emsp;การวิเคราะห์ระบบ พบว่ากลุ่มข้อมูลที่สัมพันธ์กับการใช้ระบบ ประกอบไปด้วย มหาวิทยาลัยราชภัฏ
ที่จะเป็นผู้กำหนดหน่วยงานที่รับผิดชอบ (ศจค.) และกำหนดพื้นที่ชุมชนที่ดำเนินการ เพื่อให้หน่วยพี้นที่ (อปท.)
บันทึกข้อมูลเกี่ยวกับชุมชน ส่วนหน่วยงานมหาวิทยาลัยราชภัฏจะบันทึกข้อมูล นักวิจัย ผู้เชี่ยวชาญ งานวิจัย บทความวิจัย
ลงในระบบฐานข้อมูล โดยมีกลุ่มโจทย์/ความเชี่ยวชาญ ที่เป็นข้อมูลสำหรับการเชื่อมความสัมพันธ์ของชุมชนกับข้อมูลที่
จะสนับสนุนการเพื่อการแก้ไขปัญหา ผลจากการนำไปใช้ประโยชน์จะถูกบันทึกลงในระบบเช่นเดียวกัน
ข้อมูลที่บันทึกทั้งหมดจะเชื่อมกันและมีความสัมพันธ์ ทำให้สามารถประมวลผลข้อมูลได้ 4 ด้านคือ การพัฒนาโจทย์วิจัยคุณภาพ
ข้อมูลสนับสนุนการตัดสินใจ ระบบสนับสนุนการใช้ประโยชน์ และสร้างระบบสารสนเทศนำเสนอข้อมูลสรุปของระบบ
