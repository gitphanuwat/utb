<p>โครงการวิจัย 	<b>"การพัฒนาระบบฐานข้อมูลการวิจัยและพัฒนาชุมชนท้องถิ่นน่าอยู่สำหรับเครือข่ายมหาวิทยาลัยราชภัฏ"</b></p>
<p><b>ที่มาและความสำคัญของปัญหา</b><br>
    	&emsp;&emsp;&emsp;โครงการการวิจัยและพัฒนาชุมชนท้องถิ่นน่าอยู่โดยเครือข่ายมหาวิทยาลัยราชภัฏ ได้รับการสนับสนุนโดยสำนักงานกองทุนสนับสนุนการสร้างเสริมสุขภาพ (สสส.) ซึ่งประกอบไปด้วยเครือข่ายมหาวิทยาลัยราชภัฏ และภาคีร่วมพัฒนาคือ องค์กรปกครองส่วนท้องถิ่น ส่วนท้องที่ ฝ่ายปกครอง องค์กรชุมชน และภาคราชการ โครงการนี้มีเป้าหมายให้เกิดการเรียนรู้ร่วมกันระหว่างเครือข่ายมหาวิทยาลัยราชภัฏกับชุมชนท้องถิ่น เพื่อการนำใช้ข้อมูลให้เกิดประโยชน์สู่การจัดการตนเองได้อย่างมีประสิทธิภาพและประสิทธิผล ให้บรรลุวัตถุประสงค์ในการพัฒนาระบบและกลไกการสนับสนุนการวิจัยเชิงพื้นที่ให้เกิดรูปธรรมการทำงานร่วมกันของนักวิชาการในเครือข่ายมหาวิทยาลัยราชภัฏ เครือข่ายองค์กรปกครองส่วนท้องถิ่นและภาคีในการเสริมสร้างความเข้มแข็งของชุมชน และนำใช้ข้อมูลสู่การสร้างองค์ความรู้และนวัตกรรมที่เหมาะสมสำหรับการพัฒนาพื้นที่ต้นแบบสุขภาวะ โดยพัฒนาชุดความรู้ คู่มือ องค์ความรู้และนวัตกรรม เครื่องมือทางวิชาการที่ชุมชนท้องถิ่นสามารถนำใช้ในการต่อยอดขยายผลสู่การจัดการตนเองอย่างยั่งยืน  โครงการนี้มีพื้นที่เป้าหมาย ได้แก่ องค์กรปกครองส่วนท้องถิ่นที่เป็นศูนย์จัดการเครือข่ายสุขภาวะชุมชน จำนวน ๑๐ แห่ง และองค์กรปกครองส่วนท้องถิ่นเครือข่าย จำนวน ๒๐๐ แห่ง มีกลุ่มเป้าหมายคือ นักศึกษา อาจารย์ บุคลากรขององค์กรปกครองส่วนท้องถิ่น และแกนนำชุมชน
    	การสร้างองค์ความรู้และนวัตกรรมที่เหมาะสมกับการพัฒนาเชิงพื้นที่ (Research Project) โดยใช้หลักการสนับสนุนทุนวิจัยจากโจทย์ในพื้นที่สร้างกระบวนการเรียนรู้งานวิจัยเชิงพื้นที่ระหว่างนักวิจัยของมหาวิทยาลัยและนักวิจัยในพื้นที่ ให้เกิดการสร้างองค์ความรู้และนวัตกรรมที่เหมาะสมกับการพัฒนาเชิงพื้นที่ และพัฒนาฐานข้อมูลด้านการวิจัย (ข้อมูลจากมหาวิทยาลัยราชภัฏเครือข่าย) สำหรับการจัดการตนเองและยกระดับต่อเนื่อง (Data base Development) จนสามารถพัฒนาชุดความรู้ คู่มือ เครื่องมือเพื่อสร้างองค์ความรู้ และนวัตกรรมที่เหมาะสมกับพื้นที่ได้ การพัฒนาระบบศูนย์กลางสำหรับการจัดการข้อมูลทั้งระบบจึงมีบทบาทและความจำเป็นสำหรับการสนับสนุนกระบวนการนี้ให้สำเร็จลุล่วงและเกิดการนำไปใช้ประโยชน์ได้อย่างเป็นรูปธรรม<br>
    &emsp;&emsp;&emsp;ดังนั้น ทีมผู้วิจัยจึงประยุกต์ใช้ระบบฐานข้อมูลและการพัฒนาแอพพลิเคชันสำหรับการจัดการข้อมูลที่สอดคล้องกับโครงการ ได้แก่ การบริหารข้อมูลพื้นที่ชุมชน การรวบรวมข้อมูลงานวิจัย บทความวิจัย นักวิจัย และผู้เชี่ยวชาญ ที่ประกอบไปด้วยฐานข้อมูลที่สามารถเชื่อมโยงประมวลผลข้อมูลร่วมกัน นำเข้าข้อมูลด้วยรูปแบบเดียวกัน ทำให้เกิดฐานข้อมูลอย่างเป็นระบบและสามารถจัดเก็บประวัติในการดำเนินกิจกรรมของโครงการที่สามารถเรียกแสดงข้อมูลย้อนหลังได้
</p>
